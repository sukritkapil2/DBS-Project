    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            int gd = Integer.parseInt(jTextField1.getText());
            if(gd < 1 || gd > 4)
            {
                JOptionPane.showMessageDialog(null, "Global depth out of range.\nPlease enter global depth between 1 and 4.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int ld = Integer.parseInt(jTextField2.getText());
            if(ld < 1 || ld > 4)
            {
                JOptionPane.showMessageDialog(null, "Local depth out of range.\nPlease enter local depth between 1 and 4.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if(ld > gd)
            {
                JOptionPane.showMessageDialog(null, "Local depth greater than global depth.\nGlobal depth equated to local depth.", "Information", JOptionPane.INFORMATION_MESSAGE);
                gd = ld;
            }
            int bfr = Integer.parseInt(jTextField3.getText());
            if(bfr < 1 || bfr > 7)
            {
                JOptionPane.showMessageDialog(null, "BFr out of range.\nPlease enter BFr between 1 and 7.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int maxld = 1 << ld;
            Vector<String> buckets = new Vector<>();
            for(int i = 0; i < maxld; i++)
            {
                buckets.add(Utility.toBinary(i, ld));
            }
            Simulation simulation = new Simulation(gd, buckets, bfr, new Vector<>(), this);
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Please enter valid global depth, local depth and bfr.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

